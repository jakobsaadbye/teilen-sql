import { fracMid } from "./frac.ts";

const main = () => {

    const tests = [
        ["[", "]", "5"],
        ["5", "]", "8"],
        ["8", "]", "9"],
        ["9", "]", "95"],
        ["98", "]", "99"],
        ["99", "]", "995"],

        ["1", "2", "15"],
        ["1", "15", "13"],
        ["001", "001002", "001001"],
        ["001", "001001", "0010005"],

        // OK
        ["[", "5", "3"],
        ["[", "3", "2"],
        ["[", "2", "1"],
        ["[", "1", "05"],
        ["[", "11", "105"],
        ["[", "111", "1105"],
        ["[", "05", "03"],
        ["[", "03", "02"],
        ["[", "02", "01"],
        ["[", "01", "005"],
        ["[", "001", "0005"],

        ["05", "1", "08"],
        ["055", "110", "08"],
        ["09", "1", "095"],
        ["099", "1", "0995"],
        ["0998", "1", "0999"],

        ["499", "5", "4995"],
        ["111", "1111", "11105"]
    ];

    for (const [a, b, wanted] of tests) {
        const given = fracMid(a, b);
        if (given === wanted) {
            console.log(`PASS "${a}", "${b}", "${wanted}"`);
        } else {
            console.log(`FAIL "${a}", "${b}", "${wanted}", ${given}`);
        }
    }
}

main();

